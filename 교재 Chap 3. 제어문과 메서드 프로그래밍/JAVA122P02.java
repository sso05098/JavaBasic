import java.util.Scanner;

public class JAVA122P02 {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int num = sc.nextInt();
		switch(num) {
		case 1 :
			System.out.println("아주 잘했습니다");
			break;
		case 2 :
			System.out.print("");
		case 3 :
			System.out.println("잘했습니다");
			break;
		case 4 :
			System.out.print("");
		case 5 :
			System.out.print("");
		case 6 :
			System.out.println("보통입니다");
		}

	}

}
